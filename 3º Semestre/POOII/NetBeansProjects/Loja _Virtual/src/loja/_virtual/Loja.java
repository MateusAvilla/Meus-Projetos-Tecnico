    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
          if(jTextField1.getText().equals("")||jTextField2.getText().equals("")||jTextField3.getText().equals("")
                ||jTextField4.getText().equals("")||jTextField5.getText().equals("")||jTextArea1.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Complete todos os campos para produzir.", "Bugou" , 0);}
        else{
        Loja_Virtual cliente01 = new Loja_Virtual();
        
        cliente01.setNumero(pedido);
        cliente01.setData(jLabel3.getText());
        cliente01.setNome(jTextField1.getText());
        cliente01.setSobrenome(jTextField4.getText());
        cliente01.setEndereco(jTextField2.getText());
        cliente01.setSexo((String) jComboBox1.getSelectedItem());
        cliente01.setTelefone(jTextField3.getText());
        cliente01.setEmail(jTextField5.getText());
        cliente01.setAssunto((String) jComboBox2.getSelectedItem());
        cliente01.setComentario(jTextArea1.getText());
        

        
        DefaultTableModel val =(DefaultTableModel) jTable2.getModel();
     
        val.addRow(new String [] { String.valueOf(cliente01.getNumero()),cliente01.getData(), cliente01.getNome(), cliente01.getSobrenome(), 
            cliente01.getEndereco(), cliente01.getSexo(), cliente01.getTelefone(), cliente01.getEmail(), cliente01.getAssunto(), 
            cliente01.getComentario()});
        
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField3.setText(null);
        jTextField4.setText(null);
        jTextField5.setText(null);
        jTextArea1.setText(null);
        jComboBox1.setSelectedItem("Feminino");
        jComboBox2.setSelectedItem("Comentário");
        
        pedido = pedido + 1;
        jLabel4.setText(String.valueOf(pedido));
        }
    }//GEN-LAST:event_jButton1ActionPerformed

