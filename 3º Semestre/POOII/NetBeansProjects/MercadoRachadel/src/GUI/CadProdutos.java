    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        if (!jTextField2.getText().equals("")){
            produtosbo = new ProdutosBO();
        
            //Cria o novo objeto
            ProdutoDTO produtodto = new ProdutoDTO();
            produtodto.setProduto(jTextField2.getText());
            produtodto.setTipo(jTextField1.getText());
            produtodto.setFornecedor(jTextField3.getText());
            produtodto.setSetor(listFuncionario.get(jComboBox1.getSelectedIndex()).getSetor());
            produtodto = produtosbo.procurar(produtodto);            
            
            if (produtodto != null){
                if (JOptionPane.showConfirmDialog(this, 
                        "Código existente. Deseja continuar a alteração?", 
                        "Atenção",0,0)==0){
                    if (produtosbo.alterar(carregarDTO()))
                        JOptionPane.showMessageDialog(null, "Dados alterados com sucesso.");
                    else 
                        JOptionPane.showMessageDialog(null, "Erro ao alterar os dados.");
                }
            } else {
                if (produtosbo.inserir(carregarDTO()))
                    JOptionPane.showMessageDialog(null, "Dados inseridos com sucesso.");
                else
                    JOptionPane.showMessageDialog(null, "Erro ao inserir os dados.");
                listObj = produtosbo.pesquisar();
                carregarTela(listObj.get(listObj.size()-1));
            }
            listObj = produtosbo.pesquisar();
        }        
    }//GEN-LAST:event_jButton7ActionPerformed

    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        if(jTextField2.getText().equals("")){
            JOptionPane.showMessageDialog(null,"Você precisa preencher o campo 'Produto' para selecionar o item "
                                                + "que será excluído.", "Erro!" , 0);
        }
        else{
        if(JOptionPane.showConfirmDialog(this, "Confirmar exclusão ?",
                "Atenção",0,0)==0){
            if(!jTextField2.getText().equals("")){
               produtosbo = new ProdutosBO();
                produtosbo.excluir(carregarDTO());
            }
            produtosbo = new ProdutosBO();
            listObj = produtosbo.pesquisar();
            limparCampos();
            if(listObj.size()==0){
                JOptionPane.showMessageDialog(null, "Tabela vazia. Nenhum dado encontrado.",
                        "Aviso", JOptionPane.PLAIN_MESSAGE);
            }else {
                carregarTela(listObj.get(0));
            }
            
        }else{
            JOptionPane.showMessageDialog(null, "O registro não vai ser excluído!",
                    "Aviso", JOptionPane.PLAIN_MESSAGE);
        }
        }
    }//GEN-LAST:event_jButton8ActionPerformed

