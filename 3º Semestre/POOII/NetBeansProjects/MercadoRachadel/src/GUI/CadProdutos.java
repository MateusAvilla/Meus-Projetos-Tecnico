    public void CarregarSetor(){
        funcionariobo = new FuncionarioBO();
        listFuncionario = funcionariobo.pesquisar();
        
        jComboBox1.removeAllItems();
        for (int x=0; x< listFuncionario.size(); x++){
            jComboBox1.addItem(String.valueOf(listFuncionario.get(x).getSetor()));
        }
    }
    
    
    public ProdutoDTO carregarDTO(){
        ProdutoDTO produtodto = new ProdutoDTO();
        if (jTextField2.getText().equals(""))
            produtodto.setSetor("");
        else
            produtodto.setSetor(listFuncionario.get(jComboBox1.getSelectedIndex()).getSetor());
        
        produtodto.setProduto((jTextField2.getText()));
        produtodto.setTipo(jTextField1.getText());
        produtodto.setFornecedor(jTextField1.getText());
       
        
        return produtodto;
    }
    
    public void carregarTela(ProdutoDTO produto){
        jTextField2.setText(String.valueOf(produto.getProduto()));
        jTextField1.setText(produto.getTipo());
        jTextField3.setText(produto.getFornecedor());
        jComboBox1.setSelectedIndex(produto.getSetor());
        
        
    }
    
    public void limparCampos(){
        jTextField1.setText(null);
        jTextField2.setText(null);
        jTextField3.setText(null);
        CarregarSetor();
        jTextField2.requestFocus();
    }
